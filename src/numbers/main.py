# Three Type of Numbers

a = 1  # 1. Integer Number
b = 1.1  # 2. Float Number
c = 1 + 2j  # 3. Complex Number (Form: a + bi)

print(f"a: {a}")  # Integer
print(f"b: {b}")  # Float
print(f"c: {c}")  # Complex Number
print(f"real part: {c.real}")  # Real part of complex number
print(f"img part: {c.imag}")  # Imaginary part of complex number
