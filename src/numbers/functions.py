# round() - rounding numbers
print(round(5.677))
print(round(5.25))

# abs() - absolute of a number
print(abs(-2.8))
print(abs(2))

# Math module - for complex mathematics calculations. (This module involves lot of mathematical functions and expressions)
import math

print(math.ceil(2.6))
print(math.floor(2.5))

print(f"isqrt: {math.isqrt(10)}")
print(f"factorial: {math.factorial(10)}")
