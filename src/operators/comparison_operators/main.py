# Comparison Operator

# Greater Than
print(10 > 3)

# Less Than
print(1 < 3)

# Less than or Equal
print(3 <= 5)

# Greater than oe Equal
print(5 >= 3)

# is Equal
print(1 == 10 ** 0)
print(10 == 10)
print(10 == "10")

# is Not Equal
print(2 != 10 ** 0)

print("ball" > "apple")  # After sort, ball comes after apple. So, ball is greater
print("bag" == "BAG")  # Both are not same.

print(ord("b"))
