# Escape Sequences in Python

course = "Python Programming"
course_name = 'Python Programming'

# \ is Escape Character
# \" is Escape Sequence
# \' is Escape Sequence
# \\  is Escape Sequence, to include an \
# \n is Escape Sequence, for new line

# Want insert an " (double quote) in String with ""
program_name = "Python \"Programming"

single_quote = "Python \'Program"

single_backslash = "Python \\Okay"

new_line = "Python \nGoodbye"

print(course)  # Python Programming
print(course_name)  # Python Programming
print(program_name)  # Python "Programming
print(single_quote)
print(single_backslash)
print(new_line)
